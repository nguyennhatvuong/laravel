     */
    public function index(Request $request)
    {
        if($request->ajax()){
            $query=Province::with('region','urban');
            if($request->region_id && $request->region_id!=''){
                $query=$query->where('region_id',$request->region_id);
            }
            $data=$query->get();
            return Datatables::of($data)->make();
        }
        return view('admin.region.index');
    }
    public function urban(Request $request){
        $data=Urban::where('province_id',$request->id)->get();
        return response()->json($data);
    }

